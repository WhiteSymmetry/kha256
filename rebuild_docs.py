def print_header(title: str) -> None:
    print(f"\n{'='*60}")
    print(f"  {title}")
    print('='*60)

def print_step(step: str) -> None:
    print(f"\n[→] {step}")

def run_command(cmd: list[str], cwd: Path | None = None) -> bool:
    """Komut çalıştır ve sonucu döndür."""
    try:
        result = subprocess.run(
            cmd,
            cwd=cwd,
            check=True,
            capture_output=True,
            text=True,
        )
        print(f"    ✓ Başarılı: {' '.join(cmd)}")
        return True
    except subprocess.CalledProcessError as e:
        print(f"    ✗ Hata: {e.stderr}")
        return False
    except FileNotFoundError:
        print(f"    ✗ Komut bulunamadı: {cmd[0]}")
        return False

# ======================================================================
# ANA İŞLEVLER
# ======================================================================

def clean_docs() -> None:
    """Mevcut docs/ dizinini tamamen sil."""
    print_step("Eski docs/ dizini siliniyor...")
    if DOCS_DIR.exists():
        shutil.rmtree(DOCS_DIR)
        print(f"    ✓ Silindi: {DOCS_DIR}")
    else:
        print(f"    ℹ Zaten yok: {DOCS_DIR}")

def create_structure() -> None:
    """Temel dizin yapısını oluştur."""
    print_step("Dizin yapısı oluşturuluyor...")
    SOURCE_DIR.mkdir(parents=True, exist_ok=True)
    API_DIR.mkdir(parents=True, exist_ok=True)
    (DOCS_DIR / "build").mkdir(parents=True, exist_ok=True)
    print(f"    ✓ {SOURCE_DIR}")
    print(f"    ✓ {API_DIR}")

