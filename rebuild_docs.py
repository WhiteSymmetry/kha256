def generate_api_docs() -> bool:
    """sphinx-apidoc ile API dokümantasyonu oluştur."""
    print_step("API dokümantasyonu oluşturuluyor (sphinx-apidoc)...")
    
    if not PACKAGE_DIR.exists():
        print(f"    ✗ Paket dizini bulunamadı: {PACKAGE_DIR}")
        return False
    
    cmd = [
        'sphinx-apidoc',
        '--force',              # Mevcut dosyaların üzerine yaz
        '--separate',           # Her modül için ayrı dosya
        '--module-first',       # Modül dokümantasyonunu en üste koy
        '--no-toc',             # TOC dosyası oluşturma
        '--output-dir', str(API_DIR),
        str(PACKAGE_DIR),
    ]
    
    return run_command(cmd)

