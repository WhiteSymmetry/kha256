def check_dependencies() -> bool:
    """Gerekli bağımlılıkları kontrol et."""
    print_step("Bağımlılıklar kontrol ediliyor...")
    
    required = ['sphinx', 'sphinx_rtd_theme']
    missing = []
    
    for pkg in required:
        try:
            __import__(pkg)
            print(f"    ✓ {pkg}")
        except ImportError:
            print(f"    ✗ {pkg} (eksik)")
            missing.append(pkg)
    
    if missing:
        print(f"\n⚠ Eksik paketler: {', '.join(missing)}")
        print("  Kurulum için:")
        print(f"  pip install {' '.join(missing)}")
        return False
    
    return True

# ======================================================================
# MAIN
# ======================================================================

def main() -> int:
    print_header(f"{PROJECT_NAME} Dokümantasyon Yeniden Oluşturucu")
    
    # 1. Bağımlılıkları kontrol et
    if not check_dependencies():
        return 1
    
    # 2. Eski docs'ı sil
    clean_docs()
    
    # 3. Dizin yapısını oluştur
    create_structure()
    
    # 4. conf.py oluştur
    write_conf_py()
    
    # 5. index.rst oluştur
    write_index_rst()
    
    # 6. Placeholder dosyaları oluştur
    write_placeholder_files()
    
    # 7. API dokümantasyonu oluştur
    if not generate_api_docs():
        print("\n⚠ API dokümantasyonu oluşturulamadı. Paket yolunu kontrol edin.")
    
    # 8. Makefile ve make.bat oluştur
    write_makefile()
    write_make_bat()
    
    # 9. .gitignore oluştur
    write_gitignore()
    
    # 10. Özet
    print_header("✅ TAMAMLANDI")
    print(f"""
Dokümantasyon başarıyla oluşturuldu:
  📁 {DOCS_DIR}

Sonraki Adımlar:
  1. Dokümantasyonu derle:
     cd docs && make html

  2. Tarayıcıda aç:
     xdg-open build/html/index.html  (Linux)
     open build/html/index.html      (macOS)
     start build\\html\\index.html     (Windows)

  3. Read the Docs'a yükle:
     - docs/ klasörünü Git'e commit et
     - readthedocs.org'da projeyi bağla

Oluşturulan Dosyalar:
  • docs/source/conf.py         (Sphinx yapılandırması)
  • docs/source/index.rst       (Ana sayfa)
  • docs/source/api/            (API referansları)
  • docs/Makefile               (Linux/macOS derleme)
  • docs/make.bat               (Windows derleme)
  • docs/.gitignore             (Git için)
""")
    
    return 0

if __name__ == '__main__':
    sys.exit(main())
